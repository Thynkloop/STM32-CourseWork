// Initialize Independent Watchdog (IWDG)
void IWDG_Init(uint16_t timeout_ms) {
	 // Enable write access to IWDG registers
	    IWDG->KR = 0x5555;

	    // Set prescaler explicitly (divider for the 40kHz LSI clock)
	    IWDG->PR = 4;  // Divider = 64 (uncommented this line)

	    // Set reload value
	    // For example, for 1000ms (1 second):
	    // reload = 1000 * (40000/64)/1000 = 625
	    IWDG->RLR = (timeout_ms * 40000) / (64 * 1000);

	    // Wait for registers to be updated
	    while(IWDG->SR != 0);

	    // Start the watchdog
	    IWDG->KR = 0xCCCC;
}

// Refresh window watchdog - must be called within the correct window
void IWDG_Refresh(void) {
    IWDG->KR = 0xAAAA;
}
