void TIM2_Init(void)
{
    // Enable Timer 2 clock
    RCC->APB1ENR |= RCC_APB1ENR_TIM2EN;

    // Configure Timer 2
    TIM2->PSC = 84 - 1;  			 // Prescaler to reduce TIM2 clock to 1 MHz
    TIM2->CR1 |= TIM_CR1_OPM; 		 // One-pulse mode (stops after counting to ARR)
}

void TIM2_Delay_us(uint32_t delay_us)
{
    TIM2->ARR = delay_us;            // Set auto-reload value for the delay
    TIM2->CNT = 0;                   // Reset counter
    TIM2->SR &= ~TIM_SR_UIF;         // Clear the update interrupt flag
    TIM2->CR1 |= TIM_CR1_CEN;        // Enable the timer

    // Wait for the update interrupt flag (UIF) to be set
    while (!(TIM2->SR & TIM_SR_UIF));

    // Timer automatically stops in OPM mode, no need to disable it
}
